"""
evaluation/generate_visuals.py
─────────────────────────────────────────────────────────────────────────────
Master report generator. Reads every ablation CSV produced under LOGS_DIR
and produces:

  1. A single consolidated markdown report (RESULTS/ABLATION_REPORT.md)
     with one table per ablation study, formatted for direct copy-paste
     into the manuscript's ablation section.
  2. A "master comparison" figure overlaying every ablation's best
     Accuracy/Macro-F1 arm on one chart, so the *relative* size of each
     module's contribution is visible at a glance.
  3. A resource-utilisation summary panel (params vs. latency vs.
     accuracy) across every module studied, pulling from
     compute_cost.csv.

Run this AFTER the individual ablation scripts have produced their CSVs
(or after evaluation/run_all_ablations.py, which calls this last):

    python evaluation/generate_visuals.py
─────────────────────────────────────────────────────────────────────────────
"""

import sys
import csv
from pathlib import Path

sys.path.append(str(Path(__file__).resolve().parents[1]))
from config.config import LOGS_DIR, RESULTS_DIR

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt


def _read_csv(name):
    path = LOGS_DIR / name
    if not path.exists():
        return None
    with open(path, newline="") as f:
        return list(csv.DictReader(f))


def _fmt_pct(x):
    try:
        return f"{float(x) * 100:.2f}%"
    except (TypeError, ValueError):
        return str(x)


def _fmt_f(x, n=4):
    try:
        return f"{float(x):.{n}f}"
    except (TypeError, ValueError):
        return str(x)


# ─────────────────────────────────────────────────────────────────────────
#  Markdown report sections
# ─────────────────────────────────────────────────────────────────────────

REPORT_SECTIONS = [
    ("ablation_incremental.csv",
     "1. Incremental Ablation — DenseNet201 -> +CCA -> +Graph -> +KG Gate",
     ["stage", "label", "test_acc", "test_f1", "test_prec", "test_rec",
      "n_params", "infer_ms"]),
    ("ablation_attention_variants.csv",
     "2. Attention-Module Comparison (None / SE / CBAM / ECA / CCA)",
     ["label", "test_acc", "test_f1", "test_prec", "test_rec",
      "n_params", "infer_ms"]),
    ("ablation_unet_variants.csv",
     "3. Segmentation-Backbone Comparison (U-Net family)",
     ["backbone", "dice", "iou", "precision", "recall", "n_params",
      "infer_ms"]),
    ("ablation_neurosymbolic.csv",
     "4. Neurosymbolic Layer Ablation (KG/PathNet Gate isolation)",
     ["variant", "label", "test_acc", "test_f1", "n_params"]),
    ("ablation_cca.csv",
     "5. CCA Ablation (from original ablation_cca.py)",
     None),
    ("ablation_graph.csv",
     "6. Graph-Module Ablation (from original ablation_graph.py)",
     None),
    ("ablation_loss.csv",
     "7. Loss-Function Ablation (from original ablation_loss.py)",
     None),
    ("ablation_augmentation.csv",
     "8. Augmentation-Strategy Ablation (from original ablation_augmentation.py)",
     None),
    ("ablation_other_modules.csv",
     "9. Other-Modules Ablation (TTA / class-weighting / optimiser / "
     "backbone / dropout)",
     ["sweep", "label", "test_acc", "test_f1", "per_image_ms", "n_params"]),
    ("compute_cost.csv",
     "10. Computational Cost & Resource Utilisation",
     ["module", "params", "flops", "latency_ms", "tta_8view_ms",
      "peak_mem_mb"]),
]

PCT_FIELDS = {"test_acc", "test_f1", "test_prec", "test_rec", "best_val_acc",
              "dice", "iou", "precision", "recall"}


def _rows_to_markdown_table(rows, fields=None):
    if not rows:
        return "_No data found — run the corresponding ablation script " \
               "first._\n"
    fields = fields or list(rows[0].keys())
    fields = [f for f in fields if f in rows[0]]
    header = "| " + " | ".join(fields) + " |"
    sep = "|" + "|".join(["---:"] * len(fields)) + "|"
    lines = [header, sep]
    for r in rows:
        cells = []
        for f in fields:
            v = r.get(f, "")
            if f in PCT_FIELDS:
                cells.append(_fmt_pct(v))
            elif f in ("n_params", "params"):
                try:
                    cells.append(f"{int(float(v)):,}")
                except (TypeError, ValueError):
                    cells.append(str(v))
            elif f in ("infer_ms", "latency_ms", "tta_8view_ms",
                       "per_image_ms"):
                cells.append(_fmt_f(v, 3) if v not in (None, "", "None")
                             else "—")
            else:
                cells.append(str(v))
        lines.append("| " + " | ".join(cells) + " |")
    return "\n".join(lines) + "\n"


def build_markdown_report():
    lines = ["# CRC-AI Deep Ablation Study — Consolidated Report",
             "",
             "Auto-generated by `evaluation/generate_visuals.py`. Each "
             "section corresponds to one ATR reviewer comment; copy the "
             "relevant table(s) directly into the manuscript's ablation "
             "section / supplementary material.",
             ""]

    for csv_name, title, fields in REPORT_SECTIONS:
        rows = _read_csv(csv_name)
        lines.append(f"## {title}")
        lines.append("")
        lines.append(_rows_to_markdown_table(rows, fields))
        lines.append("")

    out = RESULTS_DIR / "ABLATION_REPORT.md"
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"  Consolidated markdown report -> {out}")
    return out


# ─────────────────────────────────────────────────────────────────────────
#  Master comparison figure
# ─────────────────────────────────────────────────────────────────────────

def build_master_comparison_figure():
    incremental = _read_csv("ablation_incremental.csv") or []
    attention = _read_csv("ablation_attention_variants.csv") or []
    neurosym = _read_csv("ablation_neurosymbolic.csv") or []
    unet = _read_csv("ablation_unet_variants.csv") or []

    if not any([incremental, attention, neurosym, unet]):
        print("  [master figure] No ablation CSVs found yet — skipping. "
              "Run the individual ablation scripts first.")
        return

    fig, axes = plt.subplots(2, 2, figsize=(15, 11))
    fig.suptitle("Master Ablation Comparison — CRC-AI Deep Ablation Study",
                 fontsize=15)

    ax = axes[0, 0]
    if incremental:
        stages = [r["stage"] for r in incremental]
        acc = [float(r["test_acc"]) * 100 for r in incremental]
        f1 = [float(r["test_f1"]) * 100 for r in incremental]
        ax.plot(stages, acc, marker="o", label="Accuracy", linewidth=2)
        ax.plot(stages, f1, marker="s", label="Macro-F1", linewidth=2)
        ax.set_title("Incremental Module Addition"); ax.legend()
        ax.set_ylabel("%")
    else:
        ax.text(0.5, 0.5, "Run ablation_incremental.py",
                ha="center", va="center"); ax.axis("off")

    ax2 = axes[0, 1]
    if attention:
        labels = [r["label"] for r in attention]
        acc = [float(r["test_acc"]) * 100 for r in attention]
        ax2.bar(labels, acc, color="#4CAF50")
        ax2.set_title("Attention-Module Comparison")
        ax2.set_ylabel("Test Accuracy (%)")
    else:
        ax2.text(0.5, 0.5, "Run ablation_attention_variants.py",
                 ha="center", va="center"); ax2.axis("off")

    ax3 = axes[1, 0]
    if unet:
        names = [r["backbone"] for r in unet]
        dice = [float(r["dice"]) * 100 for r in unet]
        ax3.bar(names, dice, color="#2196F3")
        ax3.set_title("Segmentation-Backbone Dice Score")
        ax3.set_ylabel("Dice (%)")
        ax3.tick_params(axis="x", rotation=25)
    else:
        ax3.text(0.5, 0.5, "Run ablation_unet_variants.py",
                 ha="center", va="center"); ax3.axis("off")

    ax4 = axes[1, 1]
    if neurosym:
        labels = [r["variant"] for r in neurosym]
        acc = [float(r["test_acc"]) * 100 for r in neurosym]
        ax4.bar(labels, acc, color="#9C27B0")
        ax4.set_title("Neurosymbolic Gate Ablation")
        ax4.set_ylabel("Test Accuracy (%)")
        ax4.tick_params(axis="x", rotation=15)
    else:
        ax4.text(0.5, 0.5, "Run ablation_neurosymbolic.py",
                 ha="center", va="center"); ax4.axis("off")

    plt.tight_layout()
    out = RESULTS_DIR / "master_ablation_comparison.png"
    plt.savefig(str(out), dpi=150)
    plt.close()
    print(f"  Master comparison figure -> {out}")


def main():
    print("=" * 60)
    print("  GENERATING CONSOLIDATED ABLATION REPORT & VISUALS")
    print("=" * 60)
    build_markdown_report()
    build_master_comparison_figure()
    print("\n  Done. See RESULTS_DIR for ABLATION_REPORT.md and all PNGs.")


if __name__ == "__main__":
    main()
